$('.button1').click(function() {
  $(this).hide();
	$('.front1').addClass('front-open');
	$('.back1').addClass('back-open');
	$('.opened1').addClass('opened-open');
	$('.modal1').show();
	setTimeout(function() {
		$('.modal1').addClass('shadow');
	}, 1000);
	setTimeout(function() {
		$('.front1').removeClass('front-open');
		$('.back1').removeClass('back-open');
		$('.opened1').removeClass('opened-open');
	}, 1200);
  $('.wrapper1').delay(500).fadeIn();
});

$('.close1').click(function() {
	$('.wrapper1').fadeOut(300);
	$('.modal1').removeClass('shadow');
	$('.front1').addClass('front-close');
	$('.back1').addClass('back-close');
	$('.opened1').addClass('opened-close');
	setTimeout(function() {
		$('.modal1').hide();
		$('.button1').show();
		$('.front1').removeClass('front-close');
		$('.back1').removeClass('back-close');
		$('.opened1').removeClass('opened-close');
	}, 1100)
});

